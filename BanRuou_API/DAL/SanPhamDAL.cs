        }
        public List<SanPhamModel> Search(int pageIndex, int pageSize, out long total, string wine_name, string alcohol_concentration)
        {
            string msgError = "";
            total = 0;
            try
            {
                var dt = _dbHelper.ExecuteSProcedureReturnDataTable(out msgError, "sp_product_singles_search",
                    "@page_index", pageIndex,
                    "@page_size", pageSize,
                    "@wine_name", wine_name,
                    "@alcohol_concentration", alcohol_concentration);
                if (!string.IsNullOrEmpty(msgError))
                    throw new Exception(msgError);
                if (dt.Rows.Count > 0) total = (long)dt.Rows[0]["RecordCount"];
                return dt.ConvertTo<SanPhamModel>().ToList();
            }
            catch (Exception ex)
            {
                throw ex;
            }
        }

